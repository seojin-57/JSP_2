package com.reviewfinder.movie.dao;

public class MovieDTO {
	private int movie_num;
	private String movie_title;
	private String movie_date;
	private String movie_plot;
	private String movie_genre;
	private String movie_poster;
	private String movie_still_image;
	private int movie_star_rate;
	
	public int getMovie_num() {
		return movie_num;
	}
	public void setMovie_num(int movie_num) {
		this.movie_num = movie_num;
	}
	public String getMovie_title() {
		return movie_title;
	}
	public void setMovie_title(String movie_title) {
		this.movie_title = movie_title;
	}
	public String getMovie_date() {
		return movie_date;
	}
	public void setMovie_date(String movie_date) {
		this.movie_date = movie_date;
	}
	public String getMovie_plot() {
		return movie_plot;
	}
	public void setMovie_plot(String movie_plot) {
		this.movie_plot = movie_plot;
	}
	public String getMovie_genre() {
		return movie_genre;
	}
	public void setMovie_genre(String movie_genre) {
		this.movie_genre = movie_genre;
	}
	public String getMovie_poster() {
		return movie_poster;
	}
	public void setMovie_poster(String movie_poster) {
		this.movie_poster = movie_poster;
	}
	public String getMovie_still_image() {
		return movie_still_image;
	}
	public void setMovie_still_image(String movie_still_image) {
		this.movie_still_image = movie_still_image;
	}
	public int getMovie_star_rate() {
		return movie_star_rate;
	}
	public void setMovie_star_rate(int movie_star_rate) {
		this.movie_star_rate = movie_star_rate;
	}
	
	
}
